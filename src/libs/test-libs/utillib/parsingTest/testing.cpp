extern void testNative(void);
extern void testNextToken(void);
static string_q sep(120, '-');

//--------------------------------------------------------------
int main(int argc, const char *argv[]) {

    etherlib_init();

    COptions options;
    options.minArgs = 0;
    if (!options.prepareArguments(argc, argv))
        return 0;

    if (!options.parseArguments(options.commandList))
        return 0;

    cout << sep << "\n";
    switch (options.testNum) {
        case 1: testNextToken(); break;
        default: testNative(); break;
    }

    return 0;
}

extern const char* STR_WHATEVER;
//--------------------------------------------------------------
void testNative(void) {

    string_q sentence = STR_WHATEVER;
    cout << sentence << "\n";

    istringstream iss(sentence);
    vector<string> tokens{istream_iterator<string>{iss}, istream_iterator<string>{}};
    for (auto elem : tokens) {
        cout << "\t" << elem << "\n";
    }
    cout << sentence << "\n";
}

